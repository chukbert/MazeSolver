class Pen(turtle.Turtle):
    def __init__(self):
        turtle.Turtle.__init__(self)
        self.shape("square")
        self.color("white")
        self.penup()
        self.speed(0)

class Path(turtle.Turtle):
    def __init__(self):
        turtle.Turtle.__init__(self)
        self.shape("square")
        self.color("green")
        self.penup()
        self.speed(0)

def setup_maze(maze):
    for y in range(len(maze)):
        for x in range(len(maze[y])):
            character = maze[y][x]
            screen_x = -288 + (x*20)
            screen_y = 288 - (y*20)
            if character == 1:
                pen.goto(screen_x, screen_y)
                pen.stamp()
            elif character == 2:
                path.goto(screen_x, screen_y)
                path.stamp()

